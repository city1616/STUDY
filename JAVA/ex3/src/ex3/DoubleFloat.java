package ex3;

public class DoubleFloat {

	public static void main(String[] args) {
		
		float f = 1.0f / 3.0f;
		double d = 1.0 / 3.0;
		
		System.out.println("f = " + f + "\n" + "d = " + d);
		System.out.println("1 / 3 = " + 1/3);
		System.out.println("1.0f / 3.0f = " + 1.0f/3.0f);
		System.out.println("1.0 / 3.0 = " + 1.0/3.0);
		
	}

}
