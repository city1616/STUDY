package ex4;

import java.util.Scanner;

public class infor {

	public static void main(String[] args) {
		
		int i ;
		Scanner a = new Scanner(System.in);
		
		
		do {
			System.out.println("객체지향 용어 설명");
			System.out.println(" 1. 객체");
			System.out.println(" 2. 클래스");
			System.out.println(" 3. 메시지");
			System.out.println(" 4. 상속");
			System.out.print("원하는 번호를 입력하세요 : ");
			i = a.nextInt();
			System.out.println("");
			
		}while(i > 4 || i < 1);
		
		switch(i) {
		case 1 :
			System.out.println("객체란 ");
			break;
		case 2 :
			System.out.println("클래스란 ");
			break;
		case 3 :
			System.out.println("메시지란 ");
			break;
		case 4 :
			System.out.println("상속이란 ");
			break;
		default :
				System.out.println("잘못입력하셨습니다.");
				break;
		}
	}

}
