public class chap2_1_8 {

	public static void main(String[] args) {

		System.out.println("산술연산");
		System.out.println(10 + 3);
		System.out.println(10 - 3);
		System.out.println(10 * 3);
		System.out.println(10 / 3);
		System.out.println(10 % 3);

		int a = 10, b = 3;

		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);


		System.out.println(a + "+" + b + "=" + (a+b));
		System.out.println(a + "-" + b + "=" + (a-b));
		System.out.println(a + "*" + b + "=" + (a*b));
		System.out.println(a + "/" + b + "=" + (a/b));
		System.out.println(a + "%" + b + "=" + (a%b));
		//+a 연결 연산자이기 때문에 ()로 우선순위 바꾸기

		
		

	}
}